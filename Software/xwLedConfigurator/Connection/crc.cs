        public static ushort init() {
            return CRC16_START_VALUE;
        }

        public static ushort update(ushort crcOld, byte data) {
            return (ushort)((ushort)(crcOld << 8) ^ CRC16TAB[(crcOld >> 8) ^ data]);
        }

        public static ushort finish(byte crcOld) {
            return (ushort)((ushort)crcOld ^ CRC16_XOR_VALUE);
        }

        public static ushort calc(byte[] data) {
            ushort crc = init();
            for (int i = 0; i < data.Length; i++) crc = update(crc, data[i]);
            return finish((byte)crc);
        }

